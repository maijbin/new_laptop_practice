package com.codemind.b12.com.codemind.b12;

public class Reverse_num_print {

	public static void main(String[] args) {

		for(int i=0;i<=10;i++)
		{
			System.out.println("The ascending oreder is:"+i);
		}
		System.out.println("#################");
		System.out.println("Descending numbers program");
		for(int j=10;j>=0;j--)
		{
			System.out.println("The descending values:"+j);
		}
	}

}
